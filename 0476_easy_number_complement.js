var findComplement = function(num) {
  let n = 0; 
  while (n < num) {
    n = n << 1 | 1
  }
  return n - num;
};

// E.g. Input = 10;

// 0 = 0 << 1 | 1 => 00 | 1 = 01 = 1;
// 1 = 1 << 1 | 1 => 10 | 1 = 11 = 3;
// 3 = 3 << 1 | 1 => 110 | 1 = 111 = 7;
// 7 = 7 << 1 | 1 => 1110 | 1 = 1111 = 15;

// 1111(15) - 1010 (10) = 0101 (5) return 5;
